# -------- 1. File Loader --------
def load_code_files(base_dir, exts={".cpp", ".h", ".c", ".f90", ".py"}):
    """
    What does it do? (1 sentence)
    This function searches for and loads files with specific
    extensions from a given directory and its subdirectories, creating a
    generator function that returns the file path and its content.

    What are the inputs?
    base_dir: The root directory to start the search from. This is a required
    input.
    exts: The set of file extensions to look for. This is an optional input but
    has a default value of extensions {".cpp", ".h", ".c", ".f90", ".py"}.

    What are the outputs?
    The output is a generator function that yields a tuple in the form
    (full path of file, content of file) for each matching file extension
    it finds.

    """
    for root, _, files in os.walk(base_dir):
        for f in files:
            if os.path.splitext(f)[-1] in exts:
                try:
                    path = Path(root) / f
                    with open(path, "r", errors="ignore") as fh:
                        yield str(path), fh.read()
                except Exception as e:
                    print(f"Skipping {f}: {e}")


# -------- 2. Chunker --------
def chunk_text(text: str, chunk_size=1000, overlap=0):
    """
    What does it do? (1 sentence)
    The function splits the given text into smaller, overlapping chunks based
    upon the word count of the text.

    What are the inputs?
    text: This is a string representing the string text to chunk. This is a
    required input.
    chunk_size: The size of the chunk in words. This is an optional input with
    value 1000.
    overlap: The number of words that overlap between the current and the previous
    chunk. This is an optional input with default value 0.

    What are the outputs?
    The output is a list of dictionaries with 3 keys: text, start_word, and
    end_word. Simply, text is the content of the chunk as a string.
    start_word is the starting word index of the chunk in the original text.
    end_word is the ending word index of the chunk in the original text.

    """
    words = text.split()
    chunks = []
    for i in range(0, len(words), chunk_size - overlap):
        piece = " ".join(words[i:i+chunk_size])
        if piece.strip():
            chunks.append({
                "text": piece,
                "start_word": i,
                "end_word": i+chunk_size
            })
    return chunks


# -------- 3. Embedding with CodeBERT --------
def embed_texts(model, texts):
    """
    What does it do? (1 sentence)
    The function takes a pre-trained transformer model (in this case
    CodeBERT) and a list of texts and encodes the texts into numerical vector
    representations based upon the model.

    What are the inputs?
    model: A pre-trained transformer model. This is a required input.
    texts: A list of strings that need to be converted into embeddings. Each
    element of the list is a single text. This is a required input.

    What are the outputs?
    embs: A NumPy array containing the embeddings of the text. They are of size
    (number of texts, embedding_dimension), with embedding_dimension to be
    determined by the input model.

    """
    embs = model.encode(
        texts,
        batch_size=16,
        show_progress_bar=True,
        convert_to_numpy=True,
        normalize_embeddings=True  # good for cosine similarity
    )
    return embs

