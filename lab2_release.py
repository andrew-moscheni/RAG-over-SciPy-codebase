def fused_retrieval(query, k=5, k0=60):
  # typically used k0=60 for best performance
  code_list=query_index(query, topk=10*k, code_or_doc = 'code')
  doc_list=query_index(query, topk=10*k, code_or_doc = 'docs')
  all_scores = []
  for chunk_idx in set(code_list).union(set(doc_list)):
    # store index for later retrieval
    code_idx = np.where(code_list==chunk_idx)[0] if chunk_idx in code_list else -1
    doc_idx = np.where(doc_list==chunk_idx)[0] if chunk_idx in doc_list else -1
    # create each term for the summation in the RRF formula
    code_term = 1/(k0+code_idx) if code_idx != -1 else 0
    doc_term = 1/(k0+doc_idx) if doc_idx!= -1 else 0
    rrf = code_term + doc_term
    # make a tuple list with the chunk index, rrf, index in code doc, index in doc list
    all_scores.append((int(chunk_idx), float(rrf), int(code_idx!=-1), int(doc_idx!=-1)))
  # now sort the scores by rrf
  return sorted(all_scores, key=lambda elem: elem[1], reverse=True)[0:k]

