# -------- 4. Build Vector DB --------
def build_faiss_index(chunks_with_meta, dim):
    """
    What does it do? (1 sentence)
    This function takes a list of chunks with their embeddings and builds
    a FAISS index.

    What are the inputs?
    chunks_with_meta: A list of dictionaries, where each dictionary represents
    a chunk and contains an embedding for the text in the chunk. This is a
    required input.
    dim: The dimensions of the embeddings, which should match the dimensions
    of the embeddings in the chunks_with_meta input. This is a required input.

    What are the outputs?
    The function returns a FAISS index object.

    """
    index = faiss.IndexFlatL2(dim)
    embeddings = np.vstack([c["embedding"] for c in chunks_with_meta]).astype("float32")
    index.add(embeddings)
    return index



# -------- Example Query --------
def query_index(query, topk=5, code_or_doc = 'code'):
    """
    What does it do? (1-2 sentences)
    This function takes a query and a FAISS index and performs a
    similarity search to find the most relevant code or documentation snippets.
    It returns the indices of the top-ranked results from the vector database.

    What are the inputs?
    query: The user's input string. This is a required input.
    topk: The number of top results to retrieve. This is an optional input
    with default value 5.
    code_or_doc: A string to specify whether to check the code chunks or the
    documentation chunks. This is an optional argument with default value
    'code'.

    What are the outputs?
    This function outputs a NumPy array of the indices of the topk most similar
    chunks in the FAISS index. This can help retrieve the original text from the
    chunk indices.

    """

    index = faiss.read_index("%s/code_chunks.index" % code_or_doc)

    q_emb = embed_texts(models[code_or_doc],[query]).astype("float32")

    distances, indices = index.search(q_emb, topk)

    return indices[0]

